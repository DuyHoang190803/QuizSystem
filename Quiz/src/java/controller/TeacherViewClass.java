    /** 
     * Handles the HTTP <code>POST</code> method.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
        try {
            TeacherDAO dao = new TeacherDAO();
            UserDAO udao = new UserDAO();
            String a = request.getParameter("id");
            int id = Integer.parseInt(a);
            HttpSession session = request.getSession();
            Classes cl = dao.getClassesByIdClass(id);
            session.setAttribute("cl", cl);
            List<User> obj = udao.getUserByClassId(id);
            request.setAttribute("stdList", obj);
            request.getRequestDispatcher("teacher/teacher-view-class-infor.jsp").forward(request, response);
        } catch (Exception ex) {
            PrintWriter out = response.getWriter();
            out.print(ex);
        }
    }

