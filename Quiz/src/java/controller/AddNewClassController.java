    // <editor-fold defaultstate="collapsed" desc="HttpServlet methods. Click on the + sign on the left to edit the code.">
    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        request.getRequestDispatcher("teacher/teacher-create-class-page.jsp").forward(request, response);
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        try {
            HttpSession session = request.getSession();
            int teacherId = (int) session.getAttribute("user_id");
            String className = request.getParameter("class_name");
            java.util.Date date = new java.util.Date();
            java.text.SimpleDateFormat sdf = new java.text.SimpleDateFormat("yyyy");
            String currentYear = sdf.format(date);
            String semester = request.getParameter("semester") +" "+ currentYear;
            String subject = request.getParameter("subject");
            String code = request.getParameter("code");
            TeacherDAO dao = new TeacherDAO();
            Classes c = new Classes(teacherId, className, code, semester, subject);
            dao.addClass(c);
            response.sendRedirect("teachergetclass");
        } catch (Exception ex) {
            PrintWriter out = response.getWriter();
            out.print("not ok :<<  " + ex);
        }

    }

